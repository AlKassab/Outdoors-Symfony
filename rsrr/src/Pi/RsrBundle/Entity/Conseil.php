<?php

namespace Pi\RsrBundle\Entity;

/**
 * Conseil
 */
class Conseil
{
    /**
     * @var integer
     */
    private $idConseil;

    /**
     * @var string
     */
    private $contenu;


    /**
     * Get idConseil
     *
     * @return integer
     */
    public function getIdConseil()
    {
        return $this->idConseil;
    }

    /**
     * Set contenu
     *
     * @param string $contenu
     *
     * @return Conseil
     */
    public function setContenu($contenu)
    {
        $this->contenu = $contenu;

        return $this;
    }

    /**
     * Get contenu
     *
     * @return string
     */
    public function getContenu()
    {
        return $this->contenu;
    }
}
